class DataType{
  public static void main(String[] args){
    int month = 12;
    int day = 29;
    float weight = 63.0f;
    double height = 168.5;
    char bloodType = 'A';
    String name = "菅原";

    System.out.println(name);
    System.out.println(height + "cm");
    System.out.println(weight + "kg");
    System.out.println("誕生日" + month + "月" + day + "日");
    System.out.println(bloodType);
    
  }
}